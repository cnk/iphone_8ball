#import "AdminViewController.h"
#import "AppDelegate.h"

@interface AdminViewController ()
@property (strong, nonatomic) NSArray *answersArray;
@end

@implementation AdminViewController

- (void) viewDidLoad
{
    AppDelegate *appDelegate = [UIApplication sharedApplication].delegate;
    NSSortDescriptor* sortOrder = [NSSortDescriptor sortDescriptorWithKey: @"self" ascending: YES];
    self.answersArray = [appDelegate.answersArray sortedArrayUsingDescriptors: [NSArray arrayWithObject: sortOrder]];
}

#pragma mark - Table view data source

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return self.answersArray.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    
    // Configure the cell...
    cell.textLabel.text = self.answersArray[indexPath.row];
    return cell;
}

